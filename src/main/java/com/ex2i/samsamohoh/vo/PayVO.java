package com.ex2i.samsamohoh.vo;

public class PayVO {                  
	private int seq;
	private int card_type;
	private int meal_type;
	private String members;
	private String restaurant_code;
	private String card_no;
	private String settling_date;
	private int total_price;
	private String etc;
	private String reg_code;
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public int getCard_type() {
		return card_type;
	}
	public void setCard_type(int card_type) {
		this.card_type = card_type;
	}
	public int getMeal_type() {
		return meal_type;
	}
	public void setMeal_type(int meal_type) {
		this.meal_type = meal_type;
	}
	public String getMembers() {
		return members;
	}
	public void setMembers(String members) {
		this.members = members;
	}
	public String getRestaurant_code() {
		return restaurant_code;
	}
	public void setRestaurant_code(String restaurant_code) {
		this.restaurant_code = restaurant_code;
	}
	public String getCard_no() {
		return card_no;
	}
	public void setCard_no(String card_no) {
		this.card_no = card_no;
	}
	public String getSettling_date() {
		return settling_date;
	}
	public void setSettling_date(String settling_date) {
		this.settling_date = settling_date;
	}
	public int getTotal_price() {
		return total_price;
	}
	public void setTotal_price(int total_price) {
		this.total_price = total_price;
	}
	public String getEtc() {
		return etc;
	}
	public void setEtc(String etc) {
		this.etc = etc;
	}
	public String getReg_code() {
		return reg_code;
	}
	public void setReg_code(String reg_code) {
		this.reg_code = reg_code;
	}
	
	
}
